let currentLanguage = localStorage.getItem("navide-marketing-language") || (navigator.language.startsWith("zh") ? "zh" : "en");
let currentWorkflowStep = 0;

function renderWorkflow() {
  const data = workflow[currentLanguage][currentWorkflowStep];
  document.querySelector("[data-workflow-title]").textContent = data.title;
  document.querySelector("[data-workflow-state]").textContent = data.state;
  document.querySelector("[data-workflow-copy]").textContent = data.copy;
  document.querySelector("[data-workflow-event]").textContent = data.event;

  document.querySelector("[data-workflow-sessions]").innerHTML = data.cards
    .map(
      ([label, status, title, progress, meta, statusClass]) => `
        <article class="session-card">
          <div class="session-top"><span>${label}</span><span class="session-status ${statusClass}">${status}</span></div>
          <strong>${title}</strong>
          <div class="progress"><span style="width: ${progress}%"></span></div>
          <small>${meta}</small>
        </article>`,
    )
    .join("");
}

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-Hant" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[language][key]) element.innerHTML = translations[language][key];
  });
  document.querySelector("[data-language]").textContent = language === "en" ? "繁中" : "EN";
  document.title = language === "en"
    ? "Navide — The engineering instrument for the Agent era"
    : "Navide — Agent 時代的工程師利器";
  localStorage.setItem("navide-marketing-language", language);
  renderWorkflow();
}

document.querySelector("[data-language]").addEventListener("click", () => {
  applyLanguage(currentLanguage === "en" ? "zh" : "en");
});

document.querySelectorAll("[data-workflow-step]").forEach((button) => {
  button.addEventListener("click", () => {
    currentWorkflowStep = Number(button.dataset.workflowStep);
    document.querySelectorAll("[data-workflow-step]").forEach((step) => step.classList.remove("active"));
    button.classList.add("active");
    renderWorkflow();
  });
});

const menuButton = document.querySelector("[data-menu]");
const navigation = document.querySelector("[data-nav]");

menuButton.addEventListener("click", () => {
  const isOpen = menuButton.getAttribute("aria-expanded") === "true";
  menuButton.setAttribute("aria-expanded", String(!isOpen));
  navigation.classList.toggle("open", !isOpen);
});

navigation.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    menuButton.setAttribute("aria-expanded", "false");
    navigation.classList.remove("open");
  });
});

const header = document.querySelector("[data-header]");
window.addEventListener("scroll", () => header.classList.toggle("scrolled", window.scrollY > 10), { passive: true });

const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
if (reducedMotion || !("IntersectionObserver" in window)) {
  document.querySelectorAll(".reveal").forEach((element) => element.classList.add("visible"));
} else {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      });
    },
    { threshold: 0.12 },
  );
  document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));
}

